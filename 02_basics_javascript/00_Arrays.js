// array
const myArr = [1,2,3,4]
console.log(myArr)
// arrays of javascript are resizable
myArr.push(6,8,9)
console.log(myArr)

myArr.pop()
console.log(myArr)

myArr.unshift(9)
console.log(myArr)
myArr.shift(10)
console.log(myArr)

console.log(myArr.includes(8)) // this prints true
console.log(myArr.includes(0)) // this will print false

console.log(myArr.indexOf(8))

